package cn.misection.dbstudy.entity;

public class ChangeDorm {

    private String sno;

    private String pre_dorm;

    private String now_dorm;

    private String time;

    public String getSno() {
        return sno;
    }

    public void setSno(String sno) {
        this.sno = sno;
    }

    public String getPre_dorm() {
        return pre_dorm;
    }

    public void setPre_dorm(String pre_dorm) {
        this.pre_dorm = pre_dorm;
    }

    public String getNow_dorm() {
        return now_dorm;
    }

    public void setNow_dorm(String now_dorm) {
        this.now_dorm = now_dorm;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
